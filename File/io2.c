#include<stdio.h>
/* In this code we will show that the iput can be hold in the buffer and can be used for the next */
int main()
{
  int N1,N2;
  
  printf("\n Enter the first number: ");
  scanf("%d",&N1);
  printf("\n Enter the second number: ");
  scanf("%d",&N2);
  printf("\n N1+N2=%d ",N1+N2);
  
  return 0;
}